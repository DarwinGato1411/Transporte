public class DatosClienteSri {
     public Contribuyente contribuyente;
    public String deuda;
    public String impugnacion;
    public String remision;

    public Contribuyente getContribuyente() {
        return contribuyente;
    }

    public void setContribuyente(Contribuyente contribuyente) {
        this.contribuyente = contribuyente;
    }

    public String getDeuda() {
        return deuda;
    }

    public void setDeuda(String deuda) {
        this.deuda = deuda;
    }

    public String getImpugnacion() {
        return impugnacion;
    }

    public void setImpugnacion(String impugnacion) {
        this.impugnacion = impugnacion;
    }

    public String getRemision() {
        return remision;
    }

    public void setRemision(String remision) {
        this.remision = remision;
    }
    
    
    
}
